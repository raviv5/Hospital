#include "Researcher.h"
#include "Article.h"


//----------------------------------------------------------------------------//
Researcher::Researcher(const string& p_name) : Employee(p_name), arr('\n')  //ctor with parmters
{

}
//----------------------------------------------------------------------------//
Researcher::Researcher(ifstream & infile):Employee(infile) , arr('\n')
{
	fromOs(infile);
}

//----------------------------------------------------------------------------//
Array<Article>& Researcher::getArticles()
{
	return arr;
}
//----------------------------------------------------------------------------//

//----------------------------------------------------------------------------//
const int Researcher::getNumOfArticles() const
{
	return arr.getLogicalSize();
}
//----------------------------------------------------------------------------//



//----------------------------------------------------------------------------//
void Researcher::addArticle(Article article)
{
	arr += article;
}
//----------------------------------------------------------------------------//

bool Researcher::operator>(const Researcher& other)
{
	if (arr.getLogicalSize() > other.arr.getLogicalSize())
	{
		return true;
	}
	else
	{
		return false;
	}
}


